#include <unistd.h>

void	ft_putstr(char *str)
{
	int	index;

	index = 0;
	while (str[index] != '\0')
	{
		write(1, &str[index], 1);
		index++;
	}
}

int	main(int argc, char *argv[])
{
	if (argv[0] && argc)
	{
		ft_putstr(argv[0]);
	}
	write(1, "\n", 1);
}
